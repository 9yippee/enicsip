int	ft_str_is_alpha(char *str)
{
	int		i;
	char	j;

	i = 0;
	if (str[i] == '\0')
		return (1);
	while (str[i] != '\0')
	{
		j = str[i];
		if (!((j >= 'A' && j <= 'Z') || (j >= 'a' && j <= 'z')))
			return (0);
		i++;
	}
	return (1);
}
